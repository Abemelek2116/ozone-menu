/*=========================================================
    ENGINE
==========================================================*/

class OzoneI18n {

    constructor() {
        this.lang = localStorage.getItem("ozone-lang") || "en";
    }

    t(key) {
        return (OZONE_LANG[this.lang] && OZONE_LANG[this.lang][key]) ||
               (OZONE_LANG["en"][key]) ||
               key;
    }

    apply() {
        document.querySelectorAll("[data-i18n]").forEach(el => {
            const key = el.dataset.i18n;
            if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
                el.placeholder = this.t(key);
            } else if (el.tagName === "OPTION") {
                el.textContent = this.t(key);
            } else {
                el.textContent = this.t(key);
            }
        });

        // Update html[lang] attribute
        document.documentElement.lang = this.lang === "am" ? "am" : "en";

        // Update toggle button labels
        document.querySelectorAll("#languageToggle, #languageSwitcher, #mobileLangToggle").forEach(btn => {
            btn.textContent = this.lang === "en" ? "EN | አማ" : "አማ | EN";
        });

        // Re-render menu cards if the menu engine is active
        if (window.OZONE_MENU && window.OZONE_MENU.ready) {
            window.OZONE_MENU.language = this.lang;
            window.OZONE_MENU.filterAndRender();
            window.OZONE_MENU.renderCategoryUI();
        }

        // Re-render cart drawer labels
        if (window.OZONE_CART) {
            window.OZONE_CART.render();
        }

        // Dispatch event for any other listeners
        window.dispatchEvent(new CustomEvent("ozone:langchange", { detail: { lang: this.lang } }));
    }

    toggle() {
        this.lang = this.lang === "en" ? "am" : "en";
        localStorage.setItem("ozone-lang", this.lang);
        this.apply();
    }

    init() {
        this.apply();

        // Wire all language toggle buttons on the page
        document.querySelectorAll("#languageToggle, #languageSwitcher").forEach(btn => {
            btn.addEventListener("click", () => this.toggle());
        });
    }
}

/*=========================================================
    BOOT — runs on both Index.html and menu.html
==========================================================*/
document.addEventListener("DOMContentLoaded", () => {
    window.OZONE_I18N = new OzoneI18n();
    OZONE_I18N.init();
});
